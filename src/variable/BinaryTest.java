package variable;

public class BinaryTest {
    public static void main(String[] args) {
        int num = 10;
        int bNum = 0B1010;
        int oNum = 012;
        int xNum = 0XA;

        System.out.println(num); //10
        System.out.println(bNum); //10
        System.out.println(oNum); //10
        System.out.println(xNum); //10
    }
}
